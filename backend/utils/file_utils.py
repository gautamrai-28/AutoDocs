def get_language(path: Path) -> str:
    name = path.name.lower()
    if name == "dockerfile":
        return "Dockerfile"
    if name == "makefile":
        return "Makefile"
    if name == "rakefile":
        return "Ruby"
    return EXTENSION_TO_LANGUAGE.get(path.suffix.lower(), "Unknown")

