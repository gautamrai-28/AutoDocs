def get_file_role(path: Path) -> str:
    name = path.name.lower()
    stem = path.stem.lower()
    ext = path.suffix.lower()
    if name in _ENTRY_FILENAMES:
        return "entry"
    if name in _CONFIG_FILENAMES:
        return "config"
    if (
        stem.startswith("test_")
        or stem.endswith("_test")
        or stem.startswith("spec_")
        or stem.endswith("_spec")
        or "test" in stem
        or "/tests/" in str(path)
        or "/test/" in str(path)
        or "/spec/" in str(path)
    ):
        return "test"
    if ext == ".md" or name in {"readme", "readme.txt"}:
        return "docs"
    if ext in {".jsx", ".tsx", ".vue", ".svelte"}:
        return "ui"
    return "core"

_ROLE_SCORES: dict[str, int] = {
    "entry": 100, "core": 60, "ui": 50,
    "config": 40, "test": 20, "docs": 10,
}
_EXTENSION_BONUSES: dict[str, int] = {
    ".py": 10, ".js": 10, ".ts": 10, ".jsx": 8, ".tsx": 8,
    ".go": 10, ".rs": 10, ".java": 10,
}
_GENERATED_INDICATORS = ("migration", ".generated.", "autogenerated", "generated_by")

def score_file(path: Path, file_size_bytes: int = 0) -> int:
    role = get_file_role(path)
    score = _ROLE_SCORES.get(role, 30)
    score += _EXTENSION_BONUSES.get(path.suffix.lower(), 0)
    if file_size_bytes > 50_000:
        score -= 20
    if file_size_bytes > 200_000:
        score -= 40
    name_lower = path.name.lower()
    if any(ind in name_lower for ind in _GENERATED_INDICATORS):
        score -= 30
    return max(score, 0)

def should_skip_dir(dir_name: str) -> bool:
    return dir_name in SKIP_DIRS or dir_name.startswith(".")

def should_skip_file(path: Path) -> bool:
    name = path.name
    ext = path.suffix.lower()
    if name in SKIP_FILENAMES:
        return True
    if ext in SKIP_EXTENSIONS:
        return True
    if name.startswith(".") and name not in {".env.example", ".env.template", ".flake8", ".pylintrc"}:
        return True
    return False

def is_text_file(path: Path, max_check_bytes: int = 8192) -> bool:
    try:
        with open(path, "rb") as f:
            chunk = f.read(max_check_bytes)
        non_printable = sum(1 for b in chunk if b < 9 or (13 < b < 32) or b == 127)
        return non_printable / max(len(chunk), 1) < 0.30
    except (OSError, PermissionError):
        return False